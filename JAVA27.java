public class JAVA27 {
    public static void main(String args[]) {
        int a=12;
        int b=20;
        System.out.println("Value of a= "+ a);
        System.out.println("Value of b = "+ b);
        System.out.println("Equal to Relational operator= "+(a==b));
        System.out.println("Not Equal to Relational operator = "+(a!=b));
        System.out.println("Greater than Relational operator = "+(a>b));
        System.out.println("Less than Relational operator = "+(a<b));
        System.out.println("Greater than equal to Relational operator = "+(a>=b));
        System.out.println("Less than Equal to Relational operator = "+(a>b));
    }
}
